
public class Ejercicio01App {

	public static void main(String[] args) {
		
		int num1 = 5, num2 = 4;
		int suma = num1+num2;
		int resta = num1-num2;
		int multiplicacion = num1*num2;
		double division = num1/num2;
		int resto = num1%num2;
		System.out.println("El resultado de la suma de "+num1+" y "+num2+" és: "+suma);
		System.out.println("El resultado de la resta de "+num1+" y "+num2+" és: "+resta);
		System.out.println("El resultado de la multiplicación de "+num1+" por "+num2+" és: "+multiplicacion);
		System.out.println("El resultado de la división entre "+num1+" entre "+num2+" és: "+division);
		System.out.println("El resto de la división entre "+num1+" y "+num2+" és: "+resto);
		
	}

}
