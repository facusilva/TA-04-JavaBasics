
public class Ejercicio03App {

	public static void main(String[] args) {
		
		int X = 4;
		int Y = 8;
		double N = 3.55;
		double M = 4.32;
		System.out.println("El valor de la variable X és: "+X);
		System.out.println("El valor de la variable Y és: "+Y);
		System.out.println("El valor de la variable N és: "+N);
		System.out.println("El valor de la variable M és: "+M);
		System.out.println("El resultado de la suma entre X e Y és: "+(X+Y));
		System.out.println("El resultado de la diferencia entre X e Y és: "+(X-Y));
		System.out.println("El resultado de la multiplicación entre X e Y és: "+(X*Y));
		System.out.println("El resultado de la división entre X e Y és: "+(X/Y));
		System.out.println("El resto de la división entre X e Y és: "+(X%Y));
		System.out.println("El resultado de la suma entre N y M és: "+(N+M));
		System.out.println("La diferencia entre N y M és: "+(N-M));
		System.out.println("La multiplicación entre N y M da como resultado: "+(N*M));
		System.out.println("La división entre N y M da como resultado: "+(N/M));
		System.out.println("El resto de la división entre N y M és : "+(N%M));
		System.out.println("La suma entre X y N da como resultado: "+(X+N));
		System.out.println("La división entre Y y M da como resultado: "+(Y/M));
		System.out.println("El resto de la divisón entre Y y M és: "+(Y%M));
		System.out.println("El doble de la variable X és: "+(X*2));
		System.out.println("El doble de la variable Y és: "+(Y*2));
		System.out.println("El doble de la variable N és: "+(N*2));
		System.out.println("El doble de la variable M és: "+(M*2));
		System.out.println("La suma de todas las variables és: "+(X+Y+N+M));
		System.out.println("El producto de todas las variables és: "+(X*Y*N*M));
		
		
	}

}
